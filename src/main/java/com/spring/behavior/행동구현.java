package com.spring.behavior;

public class 행동구현 implements 행동 {

	@Override
	public void 잠자기() {
		System.out.println("쿨쿨쿨");
	}

	@Override
	public void 공부하기() {
		System.out.println("으앵열공해용으앵");
	}

	@Override
	public void 밥먹기() {
		System.out.println("냠냠쩝쩝");
	}

	@Override
	public void 데이트() {
		System.out.println("save the date ♡");
	}

	@Override
	public void 운동() {
		System.out.println("으랏챠챠 으쌰으쌰");
	}

	@Override
	public void 놀기() {
		System.out.println("노눈게 제에일 죠아앙");
	}

	@Override
	public void 정신수양() {
		System.out.println("옴마니반메훔~");
	}

}
